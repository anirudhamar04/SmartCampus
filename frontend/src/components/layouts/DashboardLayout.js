const DashboardLayout = () => {
  const [sidebarOpen, setSidebarOpen] = useState(false);
  const { currentUser, logout, userRole } = useAuth();
  const navigate = useNavigate();

  const handleLogout = () => {
    logout();
    navigate('/login');
  };

  // Default navigation links
  let navLinks = [
    { to: '/', label: 'Dashboard', icon: <HomeIcon /> },
    { to: '/profile', label: 'Profile', icon: <ProfileIcon /> },
  ];

  // Check user role and add appropriate links
  if (userRole === 'ADMIN') {
    navLinks.push({ to: '/users', label: 'Users', icon: <UsersIcon /> });
  } else if (userRole === 'FACULTY') {
    navLinks = [
      { to: '/teacher/dashboard', label: 'Dashboard', icon: <HomeIcon /> },
      { to: '/teacher/attendance', label: 'Attendance', icon: <AttendanceIcon /> },
      { to: '/teacher/feedback', label: 'Feedback', icon: <FeedbackIcon /> },
      { to: '/teacher/notifications', label: 'Notifications', icon: <NotificationIcon /> },
      { to: '/teacher/events', label: 'Events', icon: <EventIcon /> },
      { to: '/teacher/resources', label: 'Resources', icon: <ResourceIcon /> },
      { to: '/teacher/facilities', label: 'Facilities', icon: <FacilityIcon /> },
      { to: '/profile', label: 'Profile', icon: <ProfileIcon /> },
    ];
  } else if (userRole === 'STUDENT') {
    navLinks = [
      { to: '/student/dashboard', label: 'Dashboard', icon: <HomeIcon /> },
      { to: '/student/attendance', label: 'My Attendance', icon: <AttendanceIcon /> },
      { to: '/student/events', label: 'Campus Events', icon: <EventIcon /> },
      { to: '/student/feedback', label: 'Feedback', icon: <FeedbackIcon /> },
      { to: '/student/facilities', label: 'Facility Booking', icon: <FacilityIcon /> },
      { to: '/student/cafeteria', label: 'Cafeteria', icon: <CafeteriaIcon /> },
      { to: '/profile', label: 'Profile', icon: <ProfileIcon /> },
    ];
  }

  return (
    <div className="min-h-screen bg-primary-950 text-primary-100 flex">
      {/* Sidebar */}
      <aside 
        className={`bg-primary-900 fixed inset-y-0 left-0 z-50 w-64 transform transition-transform duration-300 ease-in-out lg:relative lg:translate-x-0 ${
          sidebarOpen ? 'translate-x-0' : '-translate-x-full'
        }`}
      >
        <div className="flex items-center justify-between h-16 px-4 border-b border-primary-800">
          <h1 className="text-xl font-bold">Smart Campus</h1>
          <button 
            className="lg:hidden text-primary-400 hover:text-primary-100"
            onClick={() => setSidebarOpen(false)}
          >
            <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor" className="w-6 h-6">
              <path fillRule="evenodd" d="M5.47 5.47a.75.75 0 011.06 0L12 10.94l5.47-5.47a.75.75 0 111.06 1.06L13.06 12l5.47 5.47a.75.75 0 11-1.06 1.06L12 13.06l-5.47 5.47a.75.75 0 01-1.06-1.06L10.94 12 5.47 6.53a.75.75 0 010-1.06z" clipRule="evenodd" />
            </svg>
          </button>
        </div>
        <nav className="mt-4 px-2">
          <ul className="space-y-2">
            {navLinks.map((link) => (
              <li key={link.to}>
                <NavLink
                  to={link.to}
                  className={({ isActive }) =>
                    `flex items-center px-4 py-3 rounded-md transition-colors ${
                      isActive
                        ? 'bg-primary-800 text-primary-100'
                        : 'text-primary-400 hover:bg-primary-800 hover:text-primary-100'
                    }`
                  }
                >
                  <span className="mr-3">{link.icon}</span>
                  <span>{link.label}</span>
                </NavLink>
              </li>
            ))}
            <li>
              <button
                onClick={handleLogout}
                className="w-full flex items-center px-4 py-3 rounded-md text-primary-400 hover:bg-primary-800 hover:text-primary-100 transition-colors"
              >
                <span className="mr-3"><LogoutIcon /></span>
                <span>Logout</span>
              </button>
            </li>
          </ul>
        </nav>
      </aside>

      {/* Main Content */}
      <div className="flex-1 flex flex-col overflow-hidden">
        {/* Header */}
        <header className="bg-primary-900 border-b border-primary-800 h-16 flex items-center px-4">
          <button
            className="text-primary-400 hover:text-primary-100 lg:hidden"
            onClick={() => setSidebarOpen(true)}
          >
            <MenuIcon />
          </button>
          <div className="ml-auto flex items-center space-x-4">
            {/* Only show notification bell for teachers/faculty */}
            {userRole === 'FACULTY' && <NotificationBell />}
            <span className="text-primary-300">
              {currentUser?.fullName || currentUser?.email}
              {userRole && ` (${userRole})`}
            </span>
            <div className="h-8 w-8 rounded-full bg-primary-700 flex items-center justify-center">
              {currentUser?.fullName?.charAt(0) || currentUser?.email?.charAt(0) || 'U'}
            </div>
          </div>
        </header>

        {/* Content */}
        <main className="flex-1 overflow-y-auto p-4 lg:p-6 bg-primary-950">
          <Outlet />
        </main>
      </div>

      {/* Overlay */}
      {sidebarOpen && (
        <div
          className="fixed inset-0 bg-black bg-opacity-50 z-40 lg:hidden"
          onClick={() => setSidebarOpen(false)}
        />
      )}
    </div>
  );
};

export default DashboardLayout; 